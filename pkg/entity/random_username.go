func DeterministicUsername(uuid string) string {
	// Determines a name given the uuid from the lists below.
	// The first two characters are the adjective
	// Look in middle for noun

	alph := shortuuid.DefaultAlphabet

	adjectiveList := strings.Split(adjectives, "\n")
	nounList := strings.Split(nouns, "\n")

	adj := 0
	for i := 0; i < 2; i++ {
		charPos := strings.Index(alph, string(uuid[i]))
		adj *= 57
		adj = adj + charPos
	}
	noun := 0
	for i := 10; i < 13; i++ {
		charPos := strings.Index(alph, string(uuid[i]))
		noun *= 57
		noun = noun + charPos
	}
	adji := adj % len(adjectiveList)
	nouni := noun % len(nounList)
	return adjectiveList[adji] + "-" + nounList[nouni]
}

